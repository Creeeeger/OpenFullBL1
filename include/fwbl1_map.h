#define UFS_HOST_COUNT 4U

static inline uint32_t mmio_read32(uint32_t addr)
{
    return readl(addr);
}

static inline void mmio_write32(uint32_t addr, uint32_t value)
{
    writel(value, addr);
}

static inline uintptr_t ufs_host_base(uint32_t host)
{
    static const uint32_t k_ufs_host_table[UFS_HOST_COUNT] = {
        0x13100000U,
        0x00000000U,
        0x00000000U,
        0x00000000U,
    };

    return (host < UFS_HOST_COUNT) ? (uintptr_t)k_ufs_host_table[host] : 0U;
}
